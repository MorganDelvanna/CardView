        //Constants for Avery Address Label 5160
        private const double PAPER_SIZE_WIDTH = 1056; //11" x 96 
        private const double PAPER_SIZE_HEIGHT =  816; //8.5" x 96

        private const double LABEL_WIDTH = 192; //2" x 96
        private const double LABEL_HEIGHT = 432; //3.5" x 96

        private const double SIDE_MARGIN = 48; //0.5" x 96
        private const double TOP_MARGIN = 72; //0.75" x 96
        private const double HORIZONTAL_GAP = 0; //0.13" x 96
        private const double VERTICAL_GAP = 0; //0.0" x 96

        private const double LABELS_PER_SHEET = 10; //2 columns of 5 labels


        private FixedPage CreatePage()
        {
            //Create new page
            FixedPage page = new FixedPage();
            //Set background
            page.Background = Brushes.White;
            //Set page size (Letter size)
            page.Width = PAPER_SIZE_WIDTH;
            page.Height = PAPER_SIZE_HEIGHT;
            return page;
        }

        public FixedDocument CreateFrontPage(DataTable data)
        {
            //Create new document
            FixedDocument doc = new FixedDocument();
            //Set page size
            doc.DocumentPaginator.PageSize = new Size(PAPER_SIZE_WIDTH, PAPER_SIZE_HEIGHT);

            Card card = new Card();
            CardLayout label = new CardLayout();

            //Number of records
            double count = (double)data.Rows.Count;

            if (count > 0)
            {
                //Determine number of pages to generate
                double pageCount = Math.Ceiling(count / LABELS_PER_SHEET);

                int dataIndex = 0;
                int currentColumn = 0;
                int currentRow = 0;

                for (int i = 0; i < pageCount; i++)
                {
                    //Create page
                    PageContent page = new PageContent();
                    FixedPage fixedPage = this.CreatePage();
                    //Create labels
                    for (int j = 0; j < 10; j++)
                    {
                        if (j % 5 == 0)
                        {
                            currentColumn = 0;
                        }
                        else
                        {
                            currentColumn++;
                        }

                        if (j < 5)
                        {
                            currentRow = 0;
                        }
                        else
                        {
                            currentRow = 1;
                        }


                        if (dataIndex < count)
                        {
                            //Get data from DataTable
                            card.FirstName = nullOrString(data.Rows[dataIndex]["FirstName"]);
                            card.LastName = nullOrString(data.Rows[dataIndex]["LastName"]);
                            card.CardNumber = nullOrString(data.Rows[dataIndex]["Card"]);
                            card.Archery = nullOrString(data.Rows[dataIndex]["Archery"]);
                            card.Handgun = nullOrString(data.Rows[dataIndex]["Handgun"]);
                            card.Action = nullOrString(data.Rows[dataIndex]["Action"]);
                            card.Smallbore = nullOrString(data.Rows[dataIndex]["Smallbore"]);
                            card.Rifle = nullOrString(data.Rows[dataIndex]["Rifle"]);
                            card.Expires = nullOrString(data.Rows[dataIndex]["Expires"]);
                            card.Picture = LoadImage(data.Rows[dataIndex]["Picture"]);

                            //Create individual label
                            label = new CardLayout(card);

                            //Set label location
                            if (currentColumn == 0)
                            {
                                FixedPage.SetLeft(label, SIDE_MARGIN);
                            }
                            else
                            {
                                FixedPage.SetLeft(label, SIDE_MARGIN + LABEL_WIDTH * currentColumn + HORIZONTAL_GAP * currentColumn);
                            }
                            if (currentRow == 0)
                            {
                                FixedPage.SetTop(label, TOP_MARGIN + currentRow * LABEL_HEIGHT);
                            }
                            else
                            {
                                FixedPage.SetTop(label, VERTICAL_GAP + currentRow * LABEL_HEIGHT);
                            }
                            

                            //Add label object to page
                            fixedPage.Children.Add(label);

                            dataIndex++;
                        }
                    }

                    //Invoke Measure(), Arrange() and UpdateLayout() for drawing
                    fixedPage.Measure(new Size(PAPER_SIZE_WIDTH, PAPER_SIZE_HEIGHT));
                    fixedPage.Arrange(new Rect(new Point(), new Size(PAPER_SIZE_WIDTH, PAPER_SIZE_HEIGHT)));
                    fixedPage.UpdateLayout();

                    ((IAddChild)page).AddChild(fixedPage);

                    doc.Pages.Add(page);
                }
            }

            return doc;
        }

